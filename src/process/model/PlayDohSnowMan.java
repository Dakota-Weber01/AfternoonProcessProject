package process.model;

public class PlayDohSnowMan
{
//Declaration Section
//Data Member Section
	private int size;
	
	public PlayDohSnowMan()
	{
		this.size = 5;
	}

	public PlayDohSnowMan(int size) 
	{
		this.size = size;
	}
	
	public String toString()
	{
	
		String description = "I am a snowman, my size is " + size;
		return description;
	}
}

			
			
		
